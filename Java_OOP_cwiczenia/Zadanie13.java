package Zadania_01_04_2023;

public class Zadanie13 {

	public static void main(String[] args) {
		
		int height = Integer.parseInt(args[1]);
		int width = Integer.parseInt(args[0]);
		
		for (int i = 1; i <= height; i++) {
			for (int j = 1; j <= width; j++) {
				System.out.print("*");
			}
			System.out.println();
			
		}
	}
	
}


