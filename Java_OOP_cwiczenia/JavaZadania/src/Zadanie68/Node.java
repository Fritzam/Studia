package Zadanie68;

class Node {
    private int value;
    private Node next;

// TODO uzupełnij kod w tym miejscu! rozwiązaniem jest tylko brakujący kod!

    Node(int value, Node next) {
        this.value = value;
        this.next = next;
    }

    int getValue() {
        return this.value;
    }

    Node getNext() {
        return next;
    }

}